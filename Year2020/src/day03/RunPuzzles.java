	static long MultiplyTreeCounts(String[] input, Point[] slopes) {
		long productCount = 1;
		TobogganTrajectory tt = new TobogganTrajectory(input);
		
		for (Point p : slopes) {
			int treeCount = tt.CountTreesOnPath(p.x, p.y);
			System.out.println("\tSlope " + p.x + ", " + p.y + " - Tree Count " + treeCount);
			productCount *= treeCount;
		}
		return productCount;
	}

